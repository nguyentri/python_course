#Định nghĩa: In ra màn hình máy tính thông tin trong dấu ngoặc ()
#(1): Có thể in ra một chuỗi (gôm trong dấu nháy đơn hoạc dấu nháy kép)
print("Chào buổi tối cả lớp!")
print('Chào buổi tối!')
#(2): Có in ra một số, không cần dấu nháy
print(12)
#(3): Có thể in ra nhiều chuỗi cách nhau bởi dấu phẩy
print("Chào buổi sáng", "ngày 15/9/2023")
#(4): Có thể in ra nhiều số hoặc cả chuỗi và số cách nhau dấu phẩy
print('Chào buổi sáng', 15, "/", 9, "/", 2023)