#Toán tử so sánh dùng để so sánh 2 giá trị
#Toán tử so sánh sẽ trả về kiểu boolean (True hoặc False)
#Nếu phép so sánh đúng, kết quả trả về là True
#Ngược lại sẽ trả về False
x = 2
y = 3
#1: So sánh bằng dùng dấu "=="
#x có bằng y hay không,nếu bằng print True, nếu không bằng thì là False
print(x==y)
#2: So sánh không bằng, "!="
print(x!=y)
#3: So sánh lớn hơn
print(x>y)
#4: So sánh nhỏ hơn
print(x<y)
#5: So sánh lơn hơn hoặc bằng
print(x>=y)
#6: So sánh bé hơn hoặc bằng
print(x<=y)
